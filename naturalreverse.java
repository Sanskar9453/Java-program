import java.util.*;
 class naturalreverse
{
	public static void main(String args[])
	{
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter Value :");
		int n = sc.nextInt();
		int i = 0; 
		while(i<=n) {
                        
			System.out.println(n);
			n--;
		}
		
	}
}